const students = [
    {
        name: "Cаша",
        grades: { математика: 90, фізика: 85, хімія: 88 }
    },
    {
        name: "Яна",
        grades: { математика: 78, фізика: 92, хімія: 81 }
    },
    {
        name: "Ілля",
        grades: { математика: 85, фізика: 80, хімія: 89 }
    },
    {
        name: "Анастасія",
        grades: { математика: 92, фізика: 88, хімія: 94 }
    }
];

function calculateAverage(grades) {
    let total = 0;
    let subjectsCount = 0;
    
    for (let subject in grades) {
        total += grades[subject];
        subjectsCount++;
    }
    
    return (total / subjectsCount).toFixed(2);
}

const results = students.map(student => {
    return {
        name: student.name,
        average: calculateAverage(student.grades)
    };
});

console.log("Список студентів та їхні середні бали:");
results.forEach(student => {
    console.log(`${student.name}: ${student.average}`);
});
